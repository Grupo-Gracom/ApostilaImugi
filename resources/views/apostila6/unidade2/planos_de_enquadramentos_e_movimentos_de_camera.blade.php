			<p>
				A noção de enquadramento é a mais importante da linguagem cinematográfi ca. Quando estudamos 
				a linguagem cinematográfi ca de planos e movimentos, fazemos referência, em primeiro lugar, 
				a um conjunto de sinais empregados em uma forma de comunicação.
